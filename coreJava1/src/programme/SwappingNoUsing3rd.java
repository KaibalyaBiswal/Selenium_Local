package programme;

public class SwappingNoUsing3rd {

	public static void main(String[] args) 
	{
int x=10;
int y=20;

System.out.println("Before Swapping :");
System.out.println(x);
System.out.println(y);


int temp=x;
x=y;
y=temp;

System.out.println("After Swapping :");
System.out.println(x);
System.out.println(y);

	}

}
